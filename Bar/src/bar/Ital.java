package bar;

public class Ital {

	private String nev;
	private Integer ar;
	private String ar1;
	private String mennyiseg;
	
	public Ital(String nev, Integer ar, String ar1, String mennyiseg) {
		this.nev = nev;
		this.ar = ar;
		this.ar1 = ar1;
		this.mennyiseg = mennyiseg;
	}

	public String getNev() {
		return nev;
	}
	public Integer getAr() {
		return ar;
	}
	public String getAr1() {
		return ar1;
	}
	public String getMennyiseg() {
		return mennyiseg;
	}
	
}
